formatted_str = ""
i = 0
while i < len(inputstr):
    if inputstr[i] == "\n":
        i += 1
    else:
        formatted_str += inputstr[i]
        i += 1

dots = []
count = 0
counting = False
for char in formatted_str:
    if char != '.' and not counting:
        counting = True
    elif char == '.':
        count += 1
        counting = True
    elif char != '.' and counting:
        dots.append(count)
        count = 0
        counting = False

dots = [i for i in dots if i != 0]
